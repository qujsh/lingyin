const { app, BrowserWindow } = require('electron');
const path = require('path');

let mainWindow;

app.whenReady().then(() => {
  mainWindow = new BrowserWindow({
    width: 800,
    height: 600,
    webPreferences: {
      nodeIntegration: true,
    },
  });

  const devServerUrl = 'http://localhost:3000'; // Next.js 默认端口
  mainWindow.loadURL(devServerUrl);
});
